#!/usr/bin/env node
/* eslint-disable no-console */

/**
 * One-shot setup script for the LIT marketing site's Resend Audience taxonomy.
 *
 * Creates (or reuses) the 12 audiences across 3 dimensions:
 *
 *   Funnel stage:    Trial / Top-100 PDF / Partner / Comparison
 *   Industry:        Forwarders / Brokers / NVOCCs / 3PLs
 *   Channel:         PPC / SEO / Social / Direct
 *
 * Idempotent — if an audience with the same name already exists in your
 * Resend workspace, we reuse its ID instead of duplicating.
 *
 * USAGE (PowerShell):
 *   $env:RESEND_API_KEY = "re_..."
 *   node scripts/setup-resend-audiences.cjs
 *
 * USAGE (bash):
 *   RESEND_API_KEY=re_... node scripts/setup-resend-audiences.cjs
 *
 * Output:
 *   - Console: paste-ready env var block
 *   - File:    .env.audiences.ready in the current directory (gitignored)
 *
 * The key never leaves your machine. The script only calls the Resend
 * REST API to create audiences — no other side-effects.
 */

const fs = require("node:fs");
const path = require("node:path");

const RESEND_API = "https://api.resend.com";

const AUDIENCES = [
  // Funnel stage
  { envVar: "RESEND_AUDIENCE_TRIAL_LEADS",  name: "LIT - Trial Leads",            group: "funnel"   },
  { envVar: "RESEND_AUDIENCE_TOP_100_PDF",  name: "LIT - Top-100 PDF Leads",      group: "funnel"   },
  { envVar: "RESEND_AUDIENCE_PARTNERS",     name: "LIT - Partner Applicants",     group: "funnel"   },
  { envVar: "RESEND_AUDIENCE_COMPARISON",   name: "LIT - Comparison Researchers", group: "funnel"   },
  // Industry / role
  { envVar: "RESEND_AUDIENCE_FORWARDERS",   name: "LIT - Freight Forwarders",     group: "industry" },
  { envVar: "RESEND_AUDIENCE_BROKERS",      name: "LIT - Freight Brokers",        group: "industry" },
  { envVar: "RESEND_AUDIENCE_NVOCCS",       name: "LIT - NVOCCs",                 group: "industry" },
  { envVar: "RESEND_AUDIENCE_3PLS",         name: "LIT - 3PLs",                   group: "industry" },
  // Channel
  { envVar: "RESEND_AUDIENCE_PPC",          name: "LIT - PPC Inbound",            group: "channel"  },
  { envVar: "RESEND_AUDIENCE_SEO",          name: "LIT - SEO Inbound",            group: "channel"  },
  { envVar: "RESEND_AUDIENCE_SOCIAL",       name: "LIT - Social Inbound",         group: "channel"  },
  { envVar: "RESEND_AUDIENCE_DIRECT",       name: "LIT - Direct / Referral",      group: "channel"  },
];

async function main() {
  const apiKey = process.env.RESEND_API_KEY;
  if (!apiKey) {
    console.error("\nERROR: RESEND_API_KEY env var is not set.\n");
    console.error("PowerShell:  $env:RESEND_API_KEY = \"re_...\"");
    console.error("bash:        export RESEND_API_KEY=re_...\n");
    process.exit(1);
  }
  if (!apiKey.startsWith("re_")) {
    console.warn("WARN: RESEND_API_KEY does not start with 're_' — continuing anyway.\n");
  }

  console.log("→ Resend Audience taxonomy setup for LIT marketing site");
  console.log("  12 audiences (funnel × industry × channel)\n");

  // 1. Fetch existing audiences so the script is idempotent.
  console.log("→ Fetching existing audiences...");
  const existing = await listAudiences(apiKey);
  const byName = new Map(existing.map((a) => [a.name, a]));
  console.log(`  Found ${existing.length} existing audience(s) in your workspace.\n`);

  // 2. Create or reuse each.
  const results = [];
  for (const aud of AUDIENCES) {
    const found = byName.get(aud.name);
    if (found) {
      console.log(`  ◇ reused  [${aud.group}] ${aud.name}  →  ${found.id}`);
      results.push({ ...aud, id: found.id, status: "reused" });
      continue;
    }
    try {
      const created = await createAudience(apiKey, aud.name);
      console.log(`  + created [${aud.group}] ${aud.name}  →  ${created.id}`);
      results.push({ ...aud, id: created.id, status: "created" });
    } catch (e) {
      console.error(`  ✗ FAILED  [${aud.group}] ${aud.name}: ${e.message}`);
      results.push({ ...aud, id: null, status: "failed", error: e.message });
    }
  }

  // 3. Emit env block.
  console.log("\n→ Paste this into Vercel → Project → Settings → Environment Variables");
  console.log("  (Apply to Production AND Preview environments)\n");

  const envLines = [];
  envLines.push("# Resend Audience IDs - generated by scripts/setup-resend-audiences.cjs");
  envLines.push(`# Generated at ${new Date().toISOString()}`);
  envLines.push("");
  envLines.push("# ── Funnel stage (drives drip sequence) ───────────────────────────");
  for (const r of results.filter((x) => x.group === "funnel")) {
    envLines.push(`${r.envVar}=${r.id ?? "<FAILED-RECREATE-MANUALLY>"}`);
  }
  envLines.push("");
  envLines.push("# ── Industry / role (broadcast targeting) ─────────────────────────");
  for (const r of results.filter((x) => x.group === "industry")) {
    envLines.push(`${r.envVar}=${r.id ?? "<FAILED-RECREATE-MANUALLY>"}`);
  }
  envLines.push("");
  envLines.push("# ── Acquisition channel (broadcast targeting) ─────────────────────");
  for (const r of results.filter((x) => x.group === "channel")) {
    envLines.push(`${r.envVar}=${r.id ?? "<FAILED-RECREATE-MANUALLY>"}`);
  }
  envLines.push("");

  const envBlock = envLines.join("\n");
  console.log(envBlock);

  // 4. Write to file for convenience.
  const outPath = path.resolve(process.cwd(), ".env.audiences.ready");
  fs.writeFileSync(outPath, envBlock + "\n", "utf8");
  console.log(`\n→ Also written to: ${outPath}`);
  console.log("  (Add this filename to .gitignore — it is environment-specific.)\n");

  // 5. Summary.
  const created = results.filter((r) => r.status === "created").length;
  const reused = results.filter((r) => r.status === "reused").length;
  const failed = results.filter((r) => r.status === "failed").length;
  console.log("→ Summary");
  console.log(`  created: ${created}`);
  console.log(`  reused:  ${reused}`);
  console.log(`  failed:  ${failed}`);
  if (failed > 0) {
    console.log("\n  ⚠ Some audiences failed. Check the API key has audiences.write permission");
    console.log("    and rerun this script — it is idempotent.\n");
    process.exit(2);
  }
  console.log("\n✓ Done. Next: paste the env block above into Vercel, then redeploy.\n");
}

// ─── Resend REST helpers ────────────────────────────────────────────────

async function listAudiences(apiKey) {
  const out = [];
  // Resend's /audiences endpoint returns up to ~100 per call. The LIT
  // workspace will be well under that for the foreseeable future, so a
  // single page is sufficient.
  const r = await fetch(`${RESEND_API}/audiences`, {
    method: "GET",
    headers: { Authorization: `Bearer ${apiKey}` },
  });
  if (!r.ok) {
    const text = await r.text().catch(() => "");
    throw new Error(`list audiences ${r.status}: ${text.slice(0, 300)}`);
  }
  const body = await r.json();
  const list = Array.isArray(body) ? body : body.data || body.audiences || [];
  for (const a of list) {
    if (a && a.id && a.name) out.push({ id: a.id, name: a.name });
  }
  return out;
}

async function createAudience(apiKey, name) {
  const r = await fetch(`${RESEND_API}/audiences`, {
    method: "POST",
    headers: {
      Authorization: `Bearer ${apiKey}`,
      "Content-Type": "application/json",
    },
    body: JSON.stringify({ name }),
  });
  if (!r.ok) {
    const text = await r.text().catch(() => "");
    throw new Error(`${r.status}: ${text.slice(0, 300)}`);
  }
  const body = await r.json();
  if (!body || !body.id) {
    throw new Error("response missing id");
  }
  return { id: body.id, name: body.name || name };
}

main().catch((e) => {
  console.error("\nFATAL:", e?.message || e);
  process.exit(1);
});
