function normalize(name) {
  return String(name || '').toLowerCase().replace(/[^a-z]/g, '');
}

const ALIAS = {
  hubspotcrm: 'hubspot',
  shopifyplus: 'shopify',
  salesforcecloud: 'salesforce',
  woo: 'woocommerce',
  bigcom: 'bigcommerce',
  amazonwebservices: 'amazon',
  aws: 'amazon',
  gcp: 'google',
  googlecloud: 'google',
  microsoftazure: 'microsoft',
  azure: 'microsoft',
  o365: 'microsoft',
  outlook: 'microsoft',
  ms: 'microsoft',
  zd: 'zendesk',
};

export default function BrandIcon({ name, size = 14, withLabel = false, className = '' }) {
  const key = ALIAS[normalize(name)] || normalize(name);
  const brand = BRAND_PATHS[key];

  if (!brand?.path) {
    // Fallback tile — keeps the row alignment intact
    const initial = String(name || '?').slice(0, 1).toUpperCase();
    return (
      <span
        title={name || 'Unknown platform'}
        className={['inline-flex items-center gap-1.5', className].join(' ')}
      >
        <span
          aria-hidden
          className="font-display flex items-center justify-center rounded-[4px] bg-slate-200 text-[9px] font-bold text-slate-500"
          style={{ width: size, height: size }}
        >
          {initial}
        </span>
        {withLabel ? (
          <span className="font-body text-[11px] text-slate-700">{name}</span>
        ) : null}
      </span>
    );
  }

  return (
    <span
      title={brand.label}
      className={['inline-flex items-center gap-1.5', className].join(' ')}
    >
      <svg
        aria-hidden
        viewBox="0 0 24 24"
        width={size}
        height={size}
        fill={brand.color}
        className="shrink-0"
      >
        <path d={brand.path} />
      </svg>
      {withLabel ? (
        <span className="font-body text-[11px] text-slate-700">{brand.label}</span>
      ) : null}
    </span>
  );
}

