function normalizeContainers(raw: any): IyCompanyContainers | null {
  const loads = Array.isArray(raw?.containers_load)
    ? raw.containers_load
    : Array.isArray(raw?.containersLoad)
      ? raw.containersLoad
      : null;
  if (!loads) return null;
  const fcl = loads.find(
    (entry: any) =>
      typeof entry?.load_type === "string" && entry.load_type.toUpperCase() === "FCL",
  );
  const lcl = loads.find(
    (entry: any) =>
      typeof entry?.load_type === "string" && entry.load_type.toUpperCase() === "LCL",
  );
  return {
    fclShipments12m:
      coerceNumber(fcl?.shipments) ??
      coerceNumber(fcl?.count) ??
      null,
    lclShipments12m:
      coerceNumber(lcl?.shipments) ??
      coerceNumber(lcl?.count) ??
      null,
  };
}

function normalizeTimeSeries(raw: any): IyTimeSeriesPoint[] {
  const dataMap = new Map<string, { fclShipments: number; lclShipments: number }>();

  const normalizeMonthLabel = (value: unknown): string | null => {
    if (typeof value !== "string" || !value.trim()) return null;
    const trimmed = value.trim();

    if (/^\d{4}-\d{2}$/.test(trimmed)) return trimmed;

    const parsed = new Date(trimmed);
    if (!Number.isNaN(parsed.getTime())) {
      return `${parsed.getFullYear()}-${String(parsed.getMonth() + 1).padStart(2, "0")}`;
    }

    return trimmed;
  };

  const upsertPoint = (monthKey: string, fcl: number, lcl: number) => {
    if (!monthKey) return;
    dataMap.set(monthKey, {
      fclShipments: Number.isFinite(fcl) ? fcl : 0,
      lclShipments: Number.isFinite(lcl) ? lcl : 0,
    });
  };

  if (Array.isArray(raw?.timeSeries)) {
    raw.timeSeries.forEach((entry: any) => {
      const monthKey = normalizeMonthLabel(entry?.month);
      if (!monthKey) return;
      upsertPoint(
        monthKey,
        coerceNumber(entry?.fclShipments) ??
          coerceNumber(entry?.fcl_shipments) ??
          coerceNumber(entry?.shipments_fcl) ??
          coerceNumber(entry?.shipments) ??
          0,
        coerceNumber(entry?.lclShipments) ??
          coerceNumber(entry?.lcl_shipments) ??
          coerceNumber(entry?.shipments_lcl) ??
          0,
      );
    });
  }

  if (Array.isArray(raw?.time_series)) {
    raw.time_series.forEach((entry: any) => {
      const monthKey = normalizeMonthLabel(
        entry?.month ?? entry?.date ?? entry?.period ?? entry?.label,
      );
      if (!monthKey) return;
      upsertPoint(
        monthKey,
        coerceNumber(entry?.fclShipments) ??
          coerceNumber(entry?.fcl_shipments) ??
          coerceNumber(entry?.shipments_fcl) ??
          coerceNumber(entry?.fcl_count) ??
          coerceNumber(entry?.fcl) ??
          coerceNumber(entry?.shipments) ??
          0,
        coerceNumber(entry?.lclShipments) ??
          coerceNumber(entry?.lcl_shipments) ??
          coerceNumber(entry?.shipments_lcl) ??
          coerceNumber(entry?.lcl_count) ??
          coerceNumber(entry?.lcl) ??
          0,
      );
    });
  } else if (raw?.time_series && typeof raw.time_series === "object") {
    Object.entries(raw.time_series).forEach(([key, value]) => {
      if (!value || typeof value !== "object") return;
      const monthKey = normalizeMonthLabel(key);
      if (!monthKey) return;

      upsertPoint(
        monthKey,
        coerceNumber((value as any).fcl_shipments) ??
          coerceNumber((value as any).shipments_fcl) ??
          coerceNumber((value as any).fcl_count) ??
          coerceNumber((value as any).fcl) ??
          coerceNumber((value as any).shipments) ??
          0,
        coerceNumber((value as any).lcl_shipments) ??
          coerceNumber((value as any).shipments_lcl) ??
          coerceNumber((value as any).lcl_count) ??
          coerceNumber((value as any).lcl) ??
          0,
      );
    });
  }

  return Array.from(dataMap.entries())
    .sort((a, b) => a[0].localeCompare(b[0]))
    .map(([month, value]) => ({
      month,
      fclShipments: value.fclShipments,
      lclShipments: value.lclShipments,
    }));
}

function cleanRouteValue(value: unknown): string | null {
  if (typeof value !== "string") return null;
  const trimmed = value.trim();
  return trimmed.length ? trimmed : null;
}

function buildRouteLabelFromEntry(entry: any): string {
  const direct =
    cleanRouteValue(entry?.route) ??
    cleanRouteValue(entry?.lane) ??
    cleanRouteValue(entry?.label);

  if (direct) return direct;

  const origin =
    cleanRouteValue(entry?.port_of_loading) ??
    cleanRouteValue(entry?.origin_port) ??
    cleanRouteValue(entry?.origin_port_name) ??
    cleanRouteValue(entry?.place_of_receipt) ??
    cleanRouteValue(entry?.supplier_address_loc) ??
    cleanRouteValue(entry?.shipper_address_loc) ??
    cleanRouteValue(entry?.origin) ??
    cleanRouteValue(entry?.origin_country) ??
    cleanRouteValue(entry?.origin_country_code);

  const destination =
    cleanRouteValue(entry?.port_of_unloading) ??
    cleanRouteValue(entry?.destination_port) ??
    cleanRouteValue(entry?.dest_port) ??
    cleanRouteValue(entry?.entry_port) ??
    cleanRouteValue(entry?.destination_port_name) ??
    cleanRouteValue(entry?.company_address_loc) ??
    cleanRouteValue(entry?.consignee_address_loc) ??
    cleanRouteValue(entry?.destination) ??
    cleanRouteValue(entry?.destination_country) ??
    cleanRouteValue(entry?.dest_country_code);

  if (origin && destination) return `${origin} → ${destination}`;
  if (origin) return origin;
  if (destination) return destination;

  return "Unknown → Unknown";
}

function normalizeTopRoutes(raw: any): IyRouteTopRoute[] {
  const source =
    raw?.routeKpis?.topRoutesLast12m ??
    raw?.top_routes ??
    raw?.topRoutes ??
    raw?.top_lanes ??
    raw?.recent_bols ??
    [];
  if (!Array.isArray(source)) return [];

  const routeMap = new Map<string, IyRouteTopRoute>();

  for (const entry of source) {
    const route = buildRouteLabelFromEntry(entry);
    const shipments =
      coerceNumber(
        entry?.shipments ??
          entry?.total_shipments ??
          entry?.shipments_12m ??
          entry?.shipments12m ??
          entry?.count,
      ) ?? 1;

    const existing = routeMap.get(route);
    if (existing) {
      existing.shipments = (existing.shipments ?? 0) + shipments;
      existing.teu =
        (existing.teu ?? 0) +
        (coerceNumber(entry?.teu ?? entry?.teu_12m ?? entry?.total_teu) ?? 0);
      existing.fclShipments =
        (existing.fclShipments ?? 0) +
        (coerceNumber(
          entry?.fclShipments ??
            entry?.fcl_shipments ??
            entry?.shipments_fcl ??
            entry?.fcl,
        ) ?? 0);
      existing.lclShipments =
        (existing.lclShipments ?? 0) +
        (coerceNumber(
          entry?.lclShipments ??
            entry?.lcl_shipments ??
            entry?.shipments_lcl ??
            entry?.lcl,
        ) ?? 0);
      continue;
    }

    routeMap.set(route, {
      route,
      shipments,
      teu: coerceNumber(entry?.teu ?? entry?.teu_12m ?? entry?.total_teu) ?? null,
      fclShipments:
        coerceNumber(
          entry?.fclShipments ??
            entry?.fcl_shipments ??
            entry?.shipments_fcl ??
            entry?.fcl,
        ) ?? null,
      lclShipments:
        coerceNumber(
          entry?.lclShipments ??
            entry?.lcl_shipments ??
            entry?.shipments_lcl ??
            entry?.lcl,
        ) ?? null,
    });
  }

  return Array.from(routeMap.values()).sort(
    (a, b) => (b.shipments ?? 0) - (a.shipments ?? 0),
  );
}

